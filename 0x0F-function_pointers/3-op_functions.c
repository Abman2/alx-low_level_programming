#include "3-calc.h"
/**
* int op_add - returns addition
* int op_sub - returns subtract
* int op_mul - returns multiply
* int op_div - returns division
* int op_mod - returns modulus
* @a: integer
* @b: integer
*/

int op_add(int a, int b)
{
return (a + b);
}
int op_sub(int a, int b)
{
return (a - b);
}
int op_mul(int a, int b)
{
return (a * b);
}
int op_div(int a, int b)
{
return (a / b);
}
int op_mod(int a, int b)
{
return (a % b);
}
